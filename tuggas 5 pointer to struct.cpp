#include <iostream>
using namespace std;

struct Jarak
{
    int meter;
    float cm;
};

int main()
{
    Jarak *ptr, d;

    ptr = &d;
    
    cout << "Masukkan Meter : ";
    cin >> (*ptr).meter;
    cout << "Masukkan Centimeter: ";
    cin >> (*ptr).cm;
 
    cout << "Menampilkan Informasi\n";
    cout << "Jarak = " << (*ptr).meter << " meter " << (*ptr).cm << " centimeter";

    return 0;
}
